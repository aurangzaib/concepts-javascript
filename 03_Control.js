let arr1 = new Array(1, 2, 3);
let obj1 = new Object({ x: 1, y: 2, z: 3 });

// ==========================================================================================================
// For (Array)
// ==========================================================================================================
for (let counter = 0; counter < arr1.length; counter += 1) {
    console.log(arr1[counter]);
}

// ==========================================================================================================
// For Each (Array)
// ==========================================================================================================
arr1.forEach(element => console.log(element));

// ==========================================================================================================
// For Of (Array)
// ==========================================================================================================
for (const element of arr1) {
    console.log(element);
}

// ==========================================================================================================
// For Of (Object)
// ==========================================================================================================
for (const [key, value] of Object.entries(obj1)) {
    console.log(key);
    console.log(value);
}

// ==========================================================================================================
// For In (Object)
// ==========================================================================================================
for (const key in obj1) {
    console.log("Key: ", key);
    console.log("Value:", obj1[key]);
}
